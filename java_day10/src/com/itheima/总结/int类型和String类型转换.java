package com.itheima.总结;

public class int类型和String类型转换 {
	public static void main(String[] args) {
		//String->int
		
		String str = "123";
		
		int i = Integer.parseInt(str);
		
		System.out.println(i);
		
		System.out.println("-----------------------");
		
		//int ->String 
		
		int a = 100;
		
		
		String str1 = String.valueOf(a);
		
		System.out.println(a);
		
		
		
		
		
	}

}
